class User {
  final int id;
  final String username;
  final String prefix;
  final double height;
  final double weight;
  final int score;
  final String avatar;
  final int year;


  User({
    required this.id,
    required this.username,
    required this.prefix,
    required this.height,
    required this.weight,
    required this.score,
    required this.avatar,
    required this.year,
  });
}